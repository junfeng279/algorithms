package com.summer.algorithms.base.sort;

/**
 * 插入排序
 * @author adminstor
 */
public class InsertSortDemo {
    public static void main(String[] args) {
        int[] a = {3, 5, 4, 1, 2, 6};
    }

    public static void insertSort(int[] a, int n){
        if(n<=1){
            return;
        }
        for(int i=1; i<n; i++){
            int value = a[i];
        }
    }
}
